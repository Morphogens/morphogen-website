<h2>Purpose</h2>
<p class="morpho-body-text text-left">
    The last few years has seen a steady stream of advances in generative machine learning for domains including images, video and audio. While traditional graphics research has pushed the limits on image quality, ML advancements promise to lower the barrier to entry for generative content.

    Morphogen is building the interfaces and metaphors that turn these technologies into playful and accessible creative tools.
